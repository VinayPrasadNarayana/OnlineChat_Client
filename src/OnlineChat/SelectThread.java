    private static void saveThreads() {
        threadserver.ThreadService_Service service = new threadserver.ThreadService_Service();
        threadserver.ThreadService port = service.getThreadServicePort();
        port.saveThreads();
    }

    private static java.util.List<threadserver.UserThread> getThreads() {
        threadserver.ThreadService_Service service = new threadserver.ThreadService_Service();
        threadserver.ThreadService port = service.getThreadServicePort();
        return port.getThreads();
    }

    private static Integer getNumOfThreads() {
        threadserver.ThreadService_Service service = new threadserver.ThreadService_Service();
        threadserver.ThreadService port = service.getThreadServicePort();
        return port.getNumOfThreads();
    }

    private static java.util.List<java.lang.Object> getThreadInfo(java.lang.Integer i) {
        threadserver.ThreadService_Service service = new threadserver.ThreadService_Service();
        threadserver.ThreadService port = service.getThreadServicePort();
        return port.getThreadInfo(i);
    }

    private static String setisEditing(java.lang.String edit) {
        threadserver.ThreadService_Service service = new threadserver.ThreadService_Service();
        threadserver.ThreadService port = service.getThreadServicePort();
        return port.setisEditing(edit);
    }

