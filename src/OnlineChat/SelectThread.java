    private void EditThreadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditThreadActionPerformed
        // TODO add your handling code here:
        Boolean editing = false;
        String title = "";
        int row = ViewThread.getSelectedRow();
        System.out.println(row);
        if (row != -1) {
            String tt = ViewThread.getModel().getValueAt(row, 0).toString();
            
            java.util.List<threadserver.UserThread> listOfThreads = getThreads();

            for (int i = 0; i < listOfThreads.size(); i++) {
                if (listOfThreads.get(i).getTitle().equals(tt)) {
                    setisEditing(listOfThreads.get(i).getTitle());
                    saveThreads();
                    System.out.println(getThreads().get(i).isIsEditing());
                    break;

                }
            }
            String User = UserLog.getText();
            new EditThread(User, editing, title).setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Please select a thread to add message");
        }
    }//GEN-LAST:event_EditThreadActionPerformed

